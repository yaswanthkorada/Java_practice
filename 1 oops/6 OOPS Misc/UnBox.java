//Wrapper class Unboxing - the automatic conversion of wrapper class into its corresponding primitive data type is called unboxing.
public class UnBox {
    public static void main(String args[]) {
        Integer a = 10;
        int a1 = (a);
        System.out.println(a1);
        Double d =  Double.valueOf(12.5647);
        double d1 = d;
        System.out.println(d1);
    }
}
