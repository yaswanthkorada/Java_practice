// initializing an object through reference variable ,initializing an object means storing data into the object
class Student {
    int id;
    String name;
}
public class Stua {
    public static void main(String args[])
     {
        Student s1=new Student();
        s1.id=10;
        s1.name="Aryan";
        System.out.println(s1.id+" "+s1.name);

     }
}
